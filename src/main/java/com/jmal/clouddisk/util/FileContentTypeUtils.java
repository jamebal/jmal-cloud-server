    private static final String UNSPLASH_MARKER = "crop&w";
    public static final String DEFAULT_CONTENT_TYPE = "application/octet-stream";
    public static final int MAX_DETECT_FILE_SIZE = 128 * 1024 * 1024;
    private static final Tika TIKA_INSTANCE = new Tika();

    private FileContentTypeUtils() {
        throw new IllegalStateException("Utility class");
    }

    /**
     * 根据文件后缀类型获取文件Content-Type
     *
     * @param extension 后缀
     * @return 文件Content-Type
     */
    public static String getContentType(String extension) {
        if (extension == null || extension.isEmpty()) {
            return DEFAULT_CONTENT_TYPE;
        }
        String extLower = extension.toLowerCase(Locale.ROOT);

        if (extLower.contains(UNSPLASH_MARKER)) {
            return "image/jpeg";
        }
        return CONTENT_TYPE_MAP.getOrDefault(extLower, DEFAULT_CONTENT_TYPE);
    }

    /**
     * 根据文件后缀类型获取文件Content-Type
     *
     * @param ext 后缀
     * @return 文件Content-Type
     */
    public static String getContentType(File file, String ext) {
        if (file == null || !file.isFile()) {
            return DEFAULT_CONTENT_TYPE;
        }
        String contentType = getContentType(ext);

        if (!contentType.equals(DEFAULT_CONTENT_TYPE)) {
            return contentType;
        }

        // 大于20M的文件返回默认类型
        if (file.length() > MAX_DETECT_FILE_SIZE) {
            return contentType;
        }

        // 如果通过扩展名得到的是默认类型，尝试使用 Tika 进行内容嗅探
        Path path = file.toPath();
        try (InputStream inputStream = Files.newInputStream(path)) {
            String tikaDetectedType = TIKA_INSTANCE.detect(inputStream, file.getName());
            if (tikaDetectedType != null && !tikaDetectedType.isEmpty()) {
                return tikaDetectedType;
            }
        } catch (Exception e) {
            return contentType;
        }

        return contentType;
    }

